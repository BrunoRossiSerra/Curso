public class Exercicio_28 {
    public static void main(String[] args) {
        int linha, coluna=10;
        int tabuada;

        for (linha = 1;linha<=10;linha++){
            for (coluna =1;coluna<=10;coluna++){
                tabuada = linha * coluna;
                System.out.println(coluna+"*"+linha+"="+tabuada);

            }
            System.out.println("------");


    }



    }
}
