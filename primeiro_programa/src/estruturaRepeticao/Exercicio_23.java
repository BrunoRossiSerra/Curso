package estruturaRepeticao;

import java.util.Random;

public class Exercicio_23 {
/*    23) Escreva um algoritmo para ler e imprimir 10 números em ordem
decrescente. (Para esse exercício, utilize WHILE).
 */
        public static void main(String[] args) {
         int incio = 0, fim =11;
         while (fim>incio){
             fim--;
             System.out.println(fim);

         }



}
}
